def fetch_rss():
    req = urllib.request.Request(RSS_URL, headers={"User-Agent": "PuddlefordBot/1.0"})
    with urllib.request.urlopen(req, timeout=30) as r:
        return r.read()


def parse_episodes(rss_bytes):
    ns = {"itunes": ITUNES_NS}
    root = ET.fromstring(rss_bytes)
    episodes = []
    for item in root.findall(".//item"):
        guid_el = item.find("guid")
        guid = (guid_el.text or "").strip() if guid_el is not None else ""
        title_el = item.find("title")
        title = (title_el.text or "Untitled").strip() if title_el is not None else "Untitled"
        link_el = item.find("link")
        link = (link_el.text or "").strip() if link_el is not None else ""
        pub_el = item.find("pubDate")
        pub_date = (pub_el.text or "").strip() if pub_el is not None else ""
        desc_el = item.find("description")
        description = desc_el.text if desc_el is not None else ""
        season_el = item.find("itunes:season", ns)
        season = season_el.text if season_el is not None else ""
        dur_el = item.find("itunes:duration", ns)
        duration = dur_el.text if dur_el is not None else ""
        img_el = item.find("itunes:image", ns)
        thumbnail = img_el.get("href", "") if img_el is not None else ""
        if not guid:
            continue
        episodes.append({
            "guid": guid,
            "title": title,
            "link": link,
            "pub_date": pub_date,
            "description": description or "",
            "season": season,
            "duration": duration,
            "thumbnail": thumbnail,
        })
    return episodes


def episode_slug(guid):
    """Same md5-of-guid slug used throughout the pipeline (episode-stats
    filenames, transcript filenames) so this generator's URLs stay stable
    across RSS title edits and line up with existing data files."""
    return hashlib.md5(guid.encode()).hexdigest()[:12]


def strip_html(desc):
    """Mirror of the client-side stripHtml(): insert a space at block
    boundaries before stripping tags, so adjacent <p>/<br> text doesn't
    glue together with no separator."""
    if not desc:
        return ""
    with_gaps = re.sub(r"<\s*br\s*/?\s*>", " ", desc, flags=re.IGNORECASE)
    with_gaps = re.sub(r"</\s*(p|div|li)\s*>", " ", with_gaps, flags=re.IGNORECASE)
    text = re.sub(r"<[^>]+>", " ", with_gaps)
    text = html.unescape(text)
    return re.sub(r"\s+", " ", text).strip()


def format_description_html(desc):
    """Mirror of the client-side formatDescriptionHtml(): if the RSS
    description has no block-level HTML tags, treat it as plain text and
    convert blank-line-separated paragraphs into real <p>/<br> markup so
    browsers don't collapse the literal newlines."""
    if not desc:
        return ""
    if re.search(r"<\s*(p|br|div|li)\b", desc, re.IGNORECASE):
        return desc
    paras = re.split(r"\n\s*\n", desc)
    return "".join(
        "<p>" + p.strip().replace("\n", "<br>") + "</p>" for p in paras if p.strip()
    )


# Canonical player first-names (as shown on players.html) mapped to the
# alternate forms that show up in RSS episode descriptions, e.g.
# "Narrated by Vicki" / "Narrated by Victoria Simpson" should both
# resolve to the same player. Longest aliases are tried first so e.g.
# "Robert Simpson" doesn't get partially matched as something else.
NARRATOR_ALIASES = {
    "Rob": ["Rob Simpson", "Robert Simpson", "Rob"],
    "Vicki": ["Vicki Simpson", "Victoria Simpson", "Vicki"],
    "Sean": ["Sean McDermott", "Sean"],
    "David": ["David Lovesy", "David"],
    "Steve": ["Steve Clark", "Steve"],
    "Jodie": ["Jodie Commerical", "Jodie Commercial", "Jodie"],
    "Finley": ["Finley Simpson", "Finley"],
    "Avalon": ["Avalon Simpson", "Avalon"],
    "Gaia": ["Gaia"],
}

# Longest-alias-first flat lookup: alias text -> canonical player name.
_NARRATOR_ALIAS_LOOKUP = sorted(
    ((alias, canon) for canon, aliases in NARRATOR_ALIASES.items() for alias in aliases),
    key=lambda pair: len(pair[0]),
    reverse=True,
)


def extract_narrator(desc_plain):
    """Look for an explicit 'Narrated by <Name>' credit in a plain-text
    (HTML-stripped) episode description and resolve it to a canonical
    player name from NARRATOR_ALIASES. Returns None if there's no such
    credit or the name doesn't resolve to a known player — deliberately
    conservative, since this feeds a public-facing 'episodes narrated by
    X' carousel theme and a wrong credit is worse than a missing one."""
    if not desc_plain:
        return None
    m = re.search(
        r"Narrat(?:ed|or)s?\s*(?:by)?[:\s]+([A-Za-z][A-Za-z .'-]*)",
        desc_plain,
        re.IGNORECASE,
    )
    if not m:
        return None
    raw = m.group(1).strip()
    # Trim off a trailing "Players:"/"Featuring:" etc. that sometimes
    # immediately follows the narrator's name with no punctuation
    # between them in the source description.
    raw = re.split(r"\s+(?:Players|Featuring|Cast)\s*:", raw, flags=re.IGNORECASE)[0].strip()
    for alias, canon in _NARRATOR_ALIAS_LOOKUP:
        if re.match(r"^" + re.escape(alias) + r"\b", raw, re.IGNORECASE):
            return canon
    return None


def load_json(path, default):
    if not path.exists():
        return default
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def esc(s):
    return html.escape(s or "", quote=True)


