#!/usr/bin/env python3
"""
Puddleford — Static episode page generator
=============================================
Generates one real, static HTML file per episode at build time, with the
correct <title>/og:*/twitter:*/JSON-LD baked directly into the <head>.

WHY THIS EXISTS
----------------
episode.html is a single static file shared by every episode; its real
per-episode title/description/artwork are only patched in by client-side
JS *after* the page loads (see the `loadPage()` IIFE in episode.html).
Social crawlers (Facebook, X/Twitter, Slack, Discord, WhatsApp, Telegram,
iMessage, etc.) do not execute JavaScript, so every shared episode link
rendered the same generic fallback card ("Episode — Puddleford", stock
cover image) instead of the real episode's title/artwork. This script
fixes that by pre-rendering a real HTML file per episode with the correct
metadata already in the markup, so crawlers (and search engines) see
the right thing without running any JS.

OUTPUT
------
  episodes/<slug>/index.html   — one per episode, slug = episode_slug(ep)
                                   (same md5-of-guid slug used elsewhere
                                   in the pipeline, e.g. episode-stats/).

The generated page is a *superset* of the old client-rendered episode.html:
it has correct meta baked in immediately, then loads the exact same client
script (unchanged from episode.html) to progressively enhance with stats,
wiki cross-references and transcript — so behaviour for a real visitor is
identical, only crawlers/link-preview bots see a difference (for the
better).

`episode.html?id=<guid>` is kept working as a redirect shim (see
episode.html's own small addition) so old shared links / bookmarks still
resolve, just without a rich preview (crawlers on *those* URLs still only
get the generic fallback, but new links generated by the site always use
the static path).

Usage:
  python3 scripts/generate_episode_pages.py
"""

