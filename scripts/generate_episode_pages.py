def build_jsonld(ep, canonical_url, image_url, desc_plain):
    schema = {
        "@context": "https://schema.org",
        "@type": "PodcastEpisode",
        "name": ep["title"],
        "url": canonical_url,
        "description": desc_plain[:300],
        "datePublished": ep["pub_date"],
        "partOfSeries": {
            "@type": "PodcastSeries",
            "@id": f"{SITE_URL}/#podcast",
        },
        "image": image_url,
        "inLanguage": "en-GB",
    }
    return json.dumps(schema, indent=2, ensure_ascii=False)


def format_date(pub_date):
    # pub_date is RFC-2822, e.g. "Fri, 10 Jul 2026 04:05:00 GMT"
    m = re.match(r"^\w+, (\d{1,2}) (\w+) (\d{4})", pub_date or "")
    if not m:
        return pub_date or ""
    day, mon, year = m.groups()
    months = {
        "Jan": "January", "Feb": "February", "Mar": "March", "Apr": "April",
        "May": "May", "Jun": "June", "Jul": "July", "Aug": "August",
        "Sep": "September", "Oct": "October", "Nov": "November", "Dec": "December",
    }
    return f"{int(day)} {months.get(mon, mon)} {year}"


def build_page(ep, prev_ep, next_ep):
    slug = episode_slug(ep["guid"])
    canonical_url = f"{SITE_URL}/episodes/{slug}/"
    image_url = ep["thumbnail"] or DEFAULT_IMAGE
    desc_plain = strip_html(ep["description"])
    desc_html = format_description_html(ep["description"])
    meta_bits = [format_date(ep["pub_date"])]
    if ep["duration"]:
        meta_bits.append(ep["duration"])
    meta_text = " \u00b7 ".join(b for b in meta_bits if b)

    badge_html = ""
    if ep["season"]:
        badge_html = f'<span id="ep-badge" class="card__badge">Season {esc(ep["season"])}</span>'

    listen_bits = []
    if ep["link"]:
        listen_bits.append(
            f'<a href="{esc(ep["link"])}" target="_blank" rel="noopener" '
            f'class="listen-btn listen-btn--primary">\u25b6 Play on Spotify</a>'
        )
    listen_bits.append(
        '<a href="https://podcasts.apple.com/podcast/puddleford/id1799546935" '
        'target="_blank" rel="noopener" class="listen-btn">Apple Podcasts</a>'
    )
    listen_bits.append(
        '<a href="https://pca.st/podcast/54648910-c521-013d-5494-0affc0b4f80b" '
        'target="_blank" rel="noopener" class="listen-btn">Pocket Casts</a>'
    )
    listen_html = "".join(listen_bits)

    def nav_link(other_ep, arrow_prefix):
        if not other_ep:
            return "#", "hidden", ""
        other_slug = episode_slug(other_ep["guid"])
        return f"/episodes/{other_slug}/", "visible", esc(other_ep["title"])

    prev_href, prev_vis, prev_title_esc = nav_link(prev_ep, "prev")
    next_href, next_vis, next_title_esc = nav_link(next_ep, "next")

    jsonld = build_jsonld(ep, canonical_url, image_url, desc_plain)

    desc_meta = desc_plain[:200] or "An episode of Puddleford: A Town with a Tale. Entirely improvised audio drama."

    return PAGE_TEMPLATE.format(
        title_esc=esc(ep["title"]),
        desc_esc=esc(desc_meta),
        canonical_url=canonical_url,
        image_url=esc(image_url),
        jsonld=jsonld,
        badge_html=badge_html,
        meta_esc=esc(meta_text),
        desc_html=desc_html,
        listen_html=listen_html,
        prev_href=prev_href,
        prev_vis=prev_vis,
        prev_title_esc=prev_title_esc,
        next_href=next_href,
        next_vis=next_vis,
        next_title_esc=next_title_esc,
        guid_esc=esc(ep["guid"]),
        title_attr_esc=esc(ep["title"]),
    )


def main():
    print("Puddleford static episode page generator")
    print("=" * 40)

    print("Fetching RSS...")
    episodes = parse_episodes(fetch_rss())
    print(f"Found {len(episodes)} episodes.")

    # Chronological order (oldest first) for prev/next nav, same as episode.html.
    # IMPORTANT: pub_date is an RFC-2822 string (e.g. "Fri, 10 Jul 2026...");
    # sorting it as a raw string gives wrong results (e.g. "10 Jul" sorts
    # before "13 Mar" alphabetically). Parse to a real datetime first.
    def pub_date_key(ep):
        try:
            return email.utils.parsedate_to_datetime(ep["pub_date"])
        except (TypeError, ValueError):
            return email.utils.parsedate_to_datetime("Thu, 01 Jan 1970 00:00:00 GMT")
    sorted_eps = sorted(episodes, key=pub_date_key)

    OUT_DIR.mkdir(parents=True, exist_ok=True)

    written = []
    for i, ep in enumerate(sorted_eps):
        prev_ep = sorted_eps[i - 1] if i > 0 else None
        next_ep = sorted_eps[i + 1] if i < len(sorted_eps) - 1 else None
        slug = episode_slug(ep["guid"])
        page_dir = OUT_DIR / slug
        page_dir.mkdir(parents=True, exist_ok=True)
        page_html = build_page(ep, prev_ep, next_ep)
        out_path = page_dir / "index.html"
        out_path.write_text(page_html, encoding="utf-8")
        written.append((ep["guid"], slug, ep["title"]))

    # Write a guid -> slug map so episode.html?id=X can redirect to the
    # matching static page, and so other pages (episodes.html cards,
    # homepage) can link straight to the static URL instead of the old
    # query-string form.
    slug_map = {guid: slug for guid, slug, _ in written}
    with open("data/episode-slugs.json", "w", encoding="utf-8") as f:
        json.dump(slug_map, f, indent=2, ensure_ascii=False)

    print(f"\nWrote {len(written)} static episode pages to {OUT_DIR}/")
    print(f"Wrote slug map to data/episode-slugs.json")

    write_episode_sitemap(written)
    write_episode_index(sorted_eps)


def _canon_episode_title(title):
    """Same normalization as scripts/update_wiki.py's helper of the same
    name: strip a trailing '(era)' suffix so title variants line up
    (RSS titles are sometimes edited to add/change an era suffix)."""
    return re.sub(r"\s*\([^)]*\)\s*$", "", title).strip().lower()


def era_to_century_label(era):
    """Bucket a wiki.json timeline 'era' string (e.g. '1830s', '1215',
    '50 AD', 'Present Day', 'The Wild West', 'Puddlefordia') into a
    coarser century-ish label for the 'small group episodes from an
    era' carousel theme. Non-numeric eras (fictional/contemporary
    settings) are passed through unchanged as their own bucket."""
    era = (era or "").strip()
    if not era:
        return None
    m = re.match(r"^(\d{1,4})", era)
    if not m:
        return era  # "Present Day", "The Wild West", "Puddlefordia", etc.
    year = int(m.group(1))
    if "AD" in era.upper() and year < 100:
        return "1st century AD"
    century_start = (year // 100) * 100
    return f"The {century_start}s"


def write_episode_index(sorted_eps):
    """Single compact per-episode data file for client-side features that
    need lightweight structured data across *all* episodes without
    re-fetching/re-parsing the full RSS feed, all 28 stats files, or
    wiki.json (e.g. the homepage themed carousel, see
    assets/js/carousel.js). Word-count / speaker stats come from
    data/episode-stats/ (already computed by scripts/update_wiki.py);
    era comes from data/wiki.json's timeline (already computed by the
    same script); everything else from RSS.
    """
    stats_index = load_json(EPISODE_STATS_INDEX, {})
    wiki = load_json(WIKI_JSON, {})
    era_by_canon_title = {
        _canon_episode_title(t.get("episode", "")): t.get("era", "")
        for t in wiki.get("timeline", [])
    }

    records = []
    for ep in sorted_eps:
        slug = episode_slug(ep["guid"])
        total_words = 0
        num_speakers = 0
        top_speaker = ""
        stats_path = stats_index.get(ep["guid"])
        if stats_path:
            stats = load_json(Path(stats_path), {})
            speaking = stats.get("speaking") or []
            if speaking:
                total_words = sum(c.get("words", 0) for c in speaking)
                num_speakers = len(speaking)
                top_speaker = speaking[0].get("name", "")
        try:
            dt = email.utils.parsedate_to_datetime(ep["pub_date"])
        except (TypeError, ValueError):
            dt = None
        era = era_by_canon_title.get(_canon_episode_title(ep["title"]), "")
        narrator = extract_narrator(strip_html(ep["description"]))
        records.append({
            "guid": ep["guid"],
            "slug": slug,
            "title": ep["title"],
            "thumbnail": ep["thumbnail"] or DEFAULT_IMAGE,
            "link": ep["link"],
            "pubDate": ep["pub_date"],
            "month": dt.month if dt else None,
            "day": dt.day if dt else None,
            "season": ep["season"],
            "duration": ep["duration"],
            "totalWords": total_words,
            "numSpeakers": num_speakers,
            "topSpeaker": top_speaker,
            "era": era,
            "eraGroup": era_to_century_label(era),
            "narrator": narrator,
        })
    # Newest first, matching every other episode listing on the site.
    records.reverse()
    with open("data/episode-index.json", "w", encoding="utf-8") as f:
        json.dump(records, f, indent=2, ensure_ascii=False)
    print(f"Wrote data/episode-index.json ({len(records)} episodes)")


def write_episode_sitemap(written):
    """Separate sitemap file for episode pages (referenced via
    <sitemapindex> style include from the main sitemap.xml), so this
    generator doesn't need to hand-merge into the manually maintained
    top-level sitemap.xml on every run."""
    import datetime
    today = datetime.date.today().isoformat()
    lines = ['<?xml version="1.0" encoding="UTF-8"?>',
             '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">']
    for _guid, slug, _title in written:
        lines.append("  <url>")
        lines.append(f"    <loc>{SITE_URL}/episodes/{slug}/</loc>")
        lines.append(f"    <lastmod>{today}</lastmod>")
        lines.append("    <changefreq>monthly</changefreq>")
        lines.append("    <priority>0.6</priority>")
        lines.append("  </url>")
    lines.append("</urlset>")
    Path("sitemap-episodes.xml").write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote sitemap-episodes.xml ({len(written)} URLs)")


